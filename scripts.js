// Arithmetic Operations Example
var a = 10, b = 20;
document.write("<h3>Arithmetic Operations:</h3>");
document.write("Addition: " + (a + b) + "<br>");
document.write("Multiplication: " + (a * b) + "<br>");

// Conditional Statement Example
var age = 18;
document.write("<h3>Conditional Statement:</h3>");
if (age >= 18) {
    document.write("Eligible for voting<br>");
} else {
    document.write("Not eligible for voting<br>");
}

// Loop Example
document.write("<h3>Loop Example:</h3>");
var count = 0;
while (count < 5) {
    document.write("Count: " + count + "<br>");
    count++;
}
